const mongoose = require('mongoose');

const SeminarSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  amount: {
    type: Number,
    required: true,
  },
});

const Seminar = mongoose.model("Seminar", SeminarSchema);

module.exports = Seminar;
